    private void tabel(){ 
        DefaultTableModel t= new DefaultTableModel();
         t.addColumn("Username"); 
         t.addColumn("Nama User"); 
         t.addColumn("Password");
         t.addColumn("level");
         jTable1.setModel(t); 
         try{ 
         res=stat.executeQuery("select * from user"); 
         while (res.next()) { 
         t.addRow(new Object[]{ res.getString("username"),
         res.getString("nama_user"),
         res.getString("password"), 
         res.getString("level") 
         }); 
         }
         }catch (Exception e) { 
         JOptionPane.showMessageDialog(rootPane, e); 
         }
    }
    
   

