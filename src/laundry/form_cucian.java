    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1){
            return;
        }        
        String nama_cucian = (String) jTable1.getValueAt(i,0);
        txt_nama_cucian.setText(nama_cucian);
        String satuan = (String) jTable1.getValueAt(i,1);
        txt_satuan.setText(satuan);
        String harga = (String) jTable1.getValueAt(i,2);
        txt_harga.setText(harga); 
        
    try { 
        res=stat.executeQuery("select * from jenis_cucian where "+ "nama_cucian='" +txt_nama_cucian.getText()
        +"'" );
        while (res.next()) 
        {
            txt_id_cucian.setText(res.getString("id_cucian"));
        }                                        
         }       
        catch (Exception e) { 
        JOptionPane.showMessageDialog(rootPane, e); 
}    
        
        
        
        
    }//GEN-LAST:event_jTable1MouseClicked

