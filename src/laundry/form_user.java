    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1){
            return;
        }
        String username = (String) jTable1.getValueAt(i,0);
        user.setText(username);
        String nama_user = (String) jTable1.getValueAt(i,1);
        nama.setText(nama_user);
        String password = (String) jTable1.getValueAt(i,2);
        pass.setText(password);
        String level = (String) jTable1.getValueAt(i,3);
        cmhak.setSelectedItem(level);
    }//GEN-LAST:event_jTable1MouseClicked

