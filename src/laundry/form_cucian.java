    private void tabel(){ 
        DefaultTableModel t= new DefaultTableModel();
         t.addColumn("Nama Cucian"); 
         t.addColumn("Satuan"); 
         t.addColumn("Harga");
         jTable1.setModel(t); 
         try{ 
         res=stat.executeQuery("select * from jenis_cucian"); 
         while (res.next()) { 
         t.addRow(new Object[]{ res.getString("nama_cucian"),
         res.getString("satuan"), 
         res.getString("biaya_cucian") 
         }); 
         }
         }catch (Exception e) { 
         JOptionPane.showMessageDialog(rootPane, e); 
         }
    }
    
